package com.HT.Variable;

public class VariableDemo1 {
    public static void main(String[] args) {
        // 目标：认识变量，掌握使用变量的好处
        // 1整数 等号是赋值的意思
        int age = 23;
        System.out.println(age);

        double score = 99.5;
        System.out.println(score);
        System.out.println(score);
        System.out.println(score);
        System.out.println(score);
        System.out.println(score);
        double money = 9.5;
        System.out.println(money);
        money = money + 100;

        System.out.println(money);


    }
}

